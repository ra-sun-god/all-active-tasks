export async function seedPlaces() {
  console.log(`Seeding ${places.length} places...`);

  // Insert in batches of 50 to avoid SQLite limits
  const BATCH = 50;
  for (let i = 0; i < places.length; i += BATCH) {
    const batch = places.slice(i, i + BATCH);
    await db.insert(entities).values(batch).onConflictDoNothing();
    console.log(`  Inserted ${Math.min(i + BATCH, places.length)} / ${places.length}`);
  }

  console.log("Seed complete.");
}

